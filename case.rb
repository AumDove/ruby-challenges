weather = 'cloudy'

case (weather)
when "raining" then puts "Wear your rain coat!"
when "snowing" then puts "Best to avoid THAT like the plague and stay in your PJ's!"
when "sunny" then puts "Grab your swimwear and let's head to the beach!"
when "foggy" then puts "Layers are best since it could change at any moment!"
when "hot" then puts "Break out those Daisy Dukes (is that still a thing?)!"
when "dark" then puts "Better grab that headlamp!"
when "cloudy" then puts "Just another Hilo day, carry on in your normal garb!"
else puts"Well, just be ready for anything and we are good to go!"
end